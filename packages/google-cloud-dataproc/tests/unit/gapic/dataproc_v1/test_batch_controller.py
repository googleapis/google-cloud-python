def test__get_api_endpoint():
    api_override = "foo.com"
    mock_client_cert_source = mock.Mock()
    default_universe = BatchControllerClient._DEFAULT_UNIVERSE
    default_endpoint = BatchControllerClient._DEFAULT_ENDPOINT_TEMPLATE.format(
        UNIVERSE_DOMAIN=default_universe
    )
    mock_universe = "bar.com"
    mock_endpoint = BatchControllerClient._DEFAULT_ENDPOINT_TEMPLATE.format(
        UNIVERSE_DOMAIN=mock_universe
    )

    assert (
        BatchControllerClient._get_api_endpoint(
            api_override, mock_client_cert_source, default_universe, "always"
        )
        == api_override
    )
    assert (
        BatchControllerClient._get_api_endpoint(
            None, mock_client_cert_source, default_universe, "auto"
        )
        == BatchControllerClient.DEFAULT_MTLS_ENDPOINT
    )
    assert (
        BatchControllerClient._get_api_endpoint(None, None, default_universe, "auto")
        == default_endpoint
    )
    assert (
        BatchControllerClient._get_api_endpoint(None, None, default_universe, "always")
        == BatchControllerClient.DEFAULT_MTLS_ENDPOINT
    )
    assert (
        BatchControllerClient._get_api_endpoint(
            None, mock_client_cert_source, default_universe, "always"
        )
        == BatchControllerClient.DEFAULT_MTLS_ENDPOINT
    )
    assert (
        BatchControllerClient._get_api_endpoint(None, None, mock_universe, "never")
        == mock_endpoint
    )
    assert (
        BatchControllerClient._get_api_endpoint(None, None, default_universe, "never")
        == default_endpoint
    )

    with pytest.raises(MutualTLSChannelError) as excinfo:
        BatchControllerClient._get_api_endpoint(
            None, mock_client_cert_source, mock_universe, "auto"
        )
    assert (
        str(excinfo.value)
        == "mTLS is not supported in any universe other than googleapis.com."
    )


