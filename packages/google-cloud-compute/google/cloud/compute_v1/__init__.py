def __getattr__(name):
    # 1. Resolve client classes
    if name in _lazy_registry:
        module_path = _lazy_registry[name]
        module = importlib.import_module(module_path, __name__)
        attribute = getattr(module, name)
        globals()[name] = attribute
        return attribute
        
    # 2. Resolve types from the compute monolith module
    try:
        types_module = importlib.import_module(".types.compute", __name__)
        if hasattr(types_module, name):
            attribute = getattr(types_module, name)
            globals()[name] = attribute
            return attribute
    except ImportError:
        pass
        
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")

