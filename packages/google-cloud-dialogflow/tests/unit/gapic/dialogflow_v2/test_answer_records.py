    transport = transports.AnswerRecordsGrpcTransport(
        credentials=ga_credentials.AnonymousCredentials(),
    )
    with pytest.raises(ValueError):
        client = AnswerRecordsClient(
            credentials=ga_credentials.AnonymousCredentials(),
            transport=transport,
        )

